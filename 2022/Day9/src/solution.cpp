/*
@brief solve part 1 of advent of code

@param filename: file to read the input from

@return ???
*/
int part1(std::string filename)
{
    ifstream infile; 
    infile.open(filename); 

    while(infile)
    {
        std::string str_input;
        std::getline(infile, str_input);
    } 

    infile.close();

    return 0;
}

